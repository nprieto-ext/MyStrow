def get_language() -> str:
    """Return current language code ('en' or 'fr')."""
    try:
        lang = _LANG_FILE.read_text(encoding="utf-8").strip().lower()
        if lang in ("en", "fr"):
            return lang
    except Exception:
        pass
    return _DEFAULT_LANG


def set_language(lang: str) -> None:
    """Persist language choice ('en' or 'fr') to disk."""
    if lang not in ("en", "fr"):
        raise ValueError(f"Unsupported language: {lang!r}  (use 'en' or 'fr')")
    _LANG_FILE.write_text(lang, encoding="utf-8")


def tr(key: str, **kwargs) -> str:
    """
    Translate *key* to the current language, formatting optional kwargs.
    Falls back to English if the key is missing for the current language.
    """
    lang = get_language()
    entry = TRANSLATIONS.get(key, {})
    text = entry.get(lang) or entry.get("en") or key
    return text.format(**kwargs) if kwargs else text
